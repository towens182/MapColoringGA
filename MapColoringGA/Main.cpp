void initPopulation(Map * individuals[])
{
	for (int i = 0; i < SIZE; i++)
	{
		individuals[i] = new Map();
	}
}

void delPopulation(Map * individuals[])
{
	for (int i = 0; i < SIZE; i++)
	{
		delete individuals[i];
	}
}

