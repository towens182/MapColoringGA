void calculateFitness(Map * individuals[], Map * parents[], int incidentMatrix[][50])
{
	for (int i = 0; i < SIZE; i++)
	{
		for (int j = 0; j < 50; j++)
		{
			for (int k = 0; k < 50; k++)
			{
				if (incidentMatrix[j][k] == 1 && individuals[i]->gene.at(j) == individuals[i]->gene.at(k))
				{
					individuals[i]->fitness++;
				}
			}
		}
	}

}

